def classify_case_type(case_type_code: str) -> Optional[str]:
    """從案號字別判定案件大類。

    Returns:
        "刑事" | "民事" | "家事" | "消費者債務清理" | "行政" | "少年" | None
        None = 歧義或無法判定
    """
    code = (case_type_code or "").strip()
    if not code:
        return None

    # 0. 歧義字別 → None
    if code in _AMBIGUOUS:
        return None

    # 1. 消費者債務清理（最明確）
    if any(code == p or code.startswith(p) for p in _CONSUMER_DEBT_PREFIXES):
        return "消費者債務清理"

    # 2. 家事（排除家護系列=民事保護令）
    if any(code.startswith(p) for p in _FAMILY_EXCLUDE_PREFIXES):
        return "民事"
    if any(code == p or code.startswith(p) for p in _FAMILY_PREFIXES):
        return "家事"

    # 3. 刑事（明確前綴）
    if any(code == p or code.startswith(p) for p in _CRIMINAL_PREFIXES):
        return "刑事"

    # 4. 少年（歸入刑事）
    if any(code == p or code.startswith(p) for p in _JUVENILE_PREFIXES):
        return "刑事"

    # 5. 行政
    if any(code.startswith(p) for p in _ADMIN_PREFIXES):
        return "行政"

    # 6. 補費（法院收費分案）
    if "補" in code:
        return "民事"

    # 7. 民事兜底
    # 注意：走到這裡的包括 簡、勞簡、國貿、保險、海商、建、消（消費訴訟非消債）、金（金融民事）等
    return "民事"


def case_type_matches_db(case_type_code: str, db_case_type: str, db_case_category: str = "") -> bool:
    """判斷案號字別的類別是否與 DB 記錄的 case_type / case_category 一致。

    用於 DB 自動回寫前的安全檢查。
    """
    email_category = classify_case_type(case_type_code)
    if email_category is None:
        return False  # 無法判定 → 保守不寫

    db_type = (db_case_type or "").strip()
    db_cat = (db_case_category or "").strip()
    _all = f"{db_type} {db_cat}"

    if email_category == "消費者債務清理":
        return any(k in _all for k in ("消費", "消債", "債務清理", "更生", "清算", "破產"))

    if email_category == "刑事":
        return any(k in _all for k in ("刑事", "刑", "偵查", "竊盜", "詐欺", "殺人", "傷害", "毒品", "洗錢", "過失致死", "性侵", "妨害"))

    if email_category == "家事":
        return any(k in _all for k in ("家事", "家", "婚姻", "離婚", "親權", "改定", "收養", "監護", "扶養"))

    if email_category == "民事":
        return any(k in _all for k in ("民事", "民", "侵權", "損害賠償", "契約", "借貸", "不動產", "勞資"))

    if email_category == "行政":
        return any(k in _all for k in ("行政", "稅", "環保"))

    return email_category in _all
